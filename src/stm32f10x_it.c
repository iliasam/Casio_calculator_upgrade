void RTCAlarm_IRQHandler(void)
{
  EXTI_ClearITPendingBit(EXTI_Line17);
  
  /* Check if the Wake-Up flag is set */
  if(PWR_GetFlagStatus(PWR_FLAG_WU) != RESET)
  {
    /* Clear Wake Up flag */
    PWR_ClearFlag(PWR_FLAG_WU);
  }
  
  RTC_WaitForLastTask();   
  RTC_ClearITPendingBit(RTC_IT_ALR);
  RTC_WaitForLastTask();

  tmp_cnt++;
}


